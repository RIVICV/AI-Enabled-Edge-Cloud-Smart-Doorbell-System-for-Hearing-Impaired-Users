# 创建完整的 HZK16 文件 (94*94*32 = 282752 字节)
font_data = bytearray(94 * 94 * 32)

for code, data in chars.items():
    zone = ((code >> 8) & 0xFF) - 0xA1
    pos = (code & 0xFF) - 0xA1
    if 0 <= zone < 94 and 0 <= pos < 94:
        offset = (zone * 94 + pos) * 32
        font_data[offset:offset+32] = bytes(data)
        print(f" 写入: 0x{code:04X} (区={zone}, 位={pos})")

with open('HZK16', 'wb') as f:
    f.write(font_data)

print(f"\n HZK16 生成成功! 大小: {len(font_data)} 字节")
