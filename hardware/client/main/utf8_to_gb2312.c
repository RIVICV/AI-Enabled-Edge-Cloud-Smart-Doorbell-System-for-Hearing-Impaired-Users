uint16_t utf8_to_gb2312(const uint8_t *utf8) {
    for (int i = 0; i < sizeof(gb_table) / sizeof(gb_table[0]); i++) {
        if (utf8[0] == gb_table[i].utf8[0] &&
            utf8[1] == gb_table[i].utf8[1] &&
            utf8[2] == gb_table[i].utf8[2]) {
            return gb_table[i].gb2312;
        }
    }
    return 0;
}